double SIN(int Xita)
{
	if(Xita < 0)
	{
		return -SIN(-Xita);
	}
	//以下θ>=0
	Xita = Xita % 360;
	//以下0<=θ<360
	if(Xita > 180)
	{
		return -SIN(Xita-180);
	}
	//以下0<=θ<180
	if(Xita > 90)
	{
		return SINDATA[180-Xita];
	}
	else
	{
		return SINDATA[Xita];
	}
	//return 2;
	
}

double COS(int Xita)
{
	return SIN(90-Xita);
}

char* itoa(int num,char* str,int radix)//此处援引csdn程序，暂时不算做代码量
{/*索引表*/
    char index[]="0123456789ABCDEF";
    unsigned unum;/*中间变量*/
    int i=0,j,k;
    /*确定unum的值*/
    if(radix==10&&num<0)/*十进制负数*/
    {
        unum=(unsigned)-num;
        str[i++]='-';
    }
    else unum=(unsigned)num;/*其他情况*/
    /*转换*/
    do{
        str[i++]=index[unum%(unsigned)radix];
        unum/=radix;
       }while(unum);
    str[i]='\0';
    /*逆序*/
    if(str[0]=='-')
        k=1;/*十进制负数*/
    else
        k=0;
     
    for(j=k;j<=(i-1)/2;j++)
    {       char temp;
        temp=str[j];
        str[j]=str[i-1+k-j];
        str[i-1+k-j]=temp;
    }
    return str;
}